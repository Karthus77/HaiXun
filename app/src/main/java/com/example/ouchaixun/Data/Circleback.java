package com.example.ouchaixun.Data;

public class Circleback {

    /**
     * code : 200
     * msg : 发布成功
     * id : 14
     */

    private int code;
    private String msg;
    private int id;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
